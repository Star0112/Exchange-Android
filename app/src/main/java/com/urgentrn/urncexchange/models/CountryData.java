package com.urgentrn.urncexchange.models;

public class CountryData {

    private String name;
    private String code;
    private String iso;
    private int ninCount;

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public String getISO() {
        return iso;
    }

    public int getNinCount() {
        return ninCount;
    }
}
